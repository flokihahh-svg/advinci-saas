from gtts import gTTS

# النص اللي نريد نحوله إلى صوت بشرى
text = "مرحباً بك في موقع أد فينسي آي، تم توليد الصوت بنجاح يا علي"

# تحويل النص إلى صوت باللغة العربية
tts = gTTS(text=text, lang='ar')

# حفظ الصوت كملف MP3 داخل المجلد
tts.save("test.mp3")

print("🔥 كفو! العملية تمت بنجاح، تفقد القائمة اليسار ستجد ملف اسمه test.mp3")